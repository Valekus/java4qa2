package LR_4;

public class Week {
    String [] weekDays = {"Monday", "Tuesday", "Wetnesday"};

    public String checkWeekDay(String weekDay){
        for (int i = 0; i<weekDay.length(); i++){
            if(weekDays[i].equals(weekDay)){
                return weekDay;
            }
            else {
                return weekDay;
            }
        }
        return weekDay;
    }
}
